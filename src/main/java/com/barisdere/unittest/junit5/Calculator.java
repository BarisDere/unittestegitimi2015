package com.barisdere.unittest.junit5;

public class Calculator {

    public int topla(int nr1, int nr2) {
        return nr1 + nr2;
    }

    public int cikar(int nr1, int nr2) {
        return nr1 - nr2;
    }

    public int bol(int nr1, int nr2) {
        return nr1 / nr2;
    }

    public int carp(int nr1, int nr2) { return nr1 * nr2; }

}
