package com.barisdere.unittest.ornek;


public class HesapMakinesi {

    public int topla(int sayi1, int sayi2) {
        return sayi1 + sayi2;
    }

    public int cikart(int sayi1, int sayi2) {
        return sayi1 - sayi2;
    }

    public int metreKareHesapla(int eni, int boyu) {
        return eni * boyu;
    }

}
